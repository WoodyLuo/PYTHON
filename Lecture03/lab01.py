import math

print( "math.sqrt(5)=", math.sqrt(5) )                                # Answer is: 2.23606797749979
print( "math.e(0.1)=", math.exp(0.1) )                                # Answer is: 1.1051709180756477
print( "math.log(math.e**2)=", math.log(math.e**2) )                  # Answer is: 2.0
print( "math.log2(10**18)=", math.log2(10**18) )                      # Answer is: 59.794705707972525
print( "math.sin(math.radians(30))=", math.sin(math.radians(30)) )    # Answer is: 0.49999999999999994
print( "math.degree(math.acos(0.5))=", math.degrees(math.acos(0.5)) ) # Answer is: 59.99999999999999
print( "math.degree(math.atan(1))=", math.degrees(math.atan(1)) )     # Answer is: 45.0